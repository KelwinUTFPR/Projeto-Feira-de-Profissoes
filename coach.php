                  <p><h2>Sobre o curso:</h2>
O coaching é uma assessoria pessoal e profissional que utiliza procedimentos orientados, cientificamente validados, para que indivíduos, times e empresas alcancem resultados superiores e positivos. Com ele, é possível entender como nós pensamos, sentimos, reagimos, aprendemos, mudamos e evoluímos.<br>

O foco das estratégias de coaching abrange, entre outros fatores, o aprofundamento da autoconsciência do cliente (coachee), por meio de ações contínuas de aprendizado. Essas ações têm como base estudos da Psicologia Cognitiva, da Psicologia Comportamental, da Psicologia Positiva, das Ciências Organizacionais e da Neurociência, além de conceitos da Filosofia, da Educação e de Administração (negócios, processos e liderança).<br>

